age = int(input('Enter your age: '))

if age > 9000:
    print('This is only 1% of my power')
elif age > 95:
    print('You are very old')
elif age > 80:
    print('You are a spring chicken')
elif age > 60:
    print('You look very young today')
elif age > 40:
    print('You don\'t look a day over 39')
else:
    print('You are but a child')
